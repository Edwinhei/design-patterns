"""
============================================================
Python 原生装饰器 · 可运行示例
============================================================

和 Java 注解完全不同的思路：
  - Java：注解是元数据 + 处理器另外写
  - Python：装饰器本身就是"包装函数"，立即生效

运行方式：
  python3 decorator_demo.py
"""

import time
from functools import wraps


# ================================================================
# 最简装饰器：函数包装函数
# ================================================================
def timer(func):
    """
    这就是装饰器的本质：接收一个函数，返回一个新函数。
    新函数在调用 func 前后加了耗时统计。
    """
    @wraps(func)  # 保留原函数的名字 / docstring 等
    def wrapper(*args, **kwargs):
        start = time.time()
        result = func(*args, **kwargs)
        cost_ms = (time.time() - start) * 1000
        print(f"⏱  {func.__name__} 耗时 {cost_ms:.1f} ms")
        return result
    return wrapper


# ================================================================
# 带参数的装饰器（装饰器工厂）
# ================================================================
def log(tag: str):
    """
    装饰器工厂：返回一个装饰器。
    这就是为什么 @log("下单") 比 @timer 多一层括号。
    """
    def decorator(func):
        @wraps(func)
        def wrapper(*args, **kwargs):
            print(f"[{tag}] 调用 {func.__name__}")
            return func(*args, **kwargs)
        return wrapper
    return decorator


# ================================================================
# 使用装饰器
# ================================================================
@timer
def place_order():
    """模拟下单"""
    time.sleep(0.12)
    print("  订单已创建")


@log("退款业务")
@timer  # 可以组合多个装饰器（从下往上应用）
def refund():
    """模拟退款"""
    time.sleep(0.05)
    print("  退款处理完成")


def normal_method():
    """没装饰器的普通方法"""
    print("  普通方法，无日志")


# ================================================================
# 主程序
# ================================================================
if __name__ == "__main__":
    print("=== @timer 单装饰器 ===")
    place_order()

    print("\n=== @log + @timer 组合装饰器 ===")
    refund()

    print("\n=== 普通方法（无装饰器）===")
    normal_method()

    # ============================================================
    # 揭秘：装饰器究竟是什么？
    # ============================================================
    print("\n=== 装饰器揭秘 ===")

    # @timer
    # def foo(): ...
    # 等价于：
    def foo():
        time.sleep(0.05)
        print("  foo 执行")

    foo = timer(foo)    # 👈 就是这一行！
    foo()

    print("\n✅ 装饰器原理小结：")
    print("   ① Python 装饰器 = 高阶函数（函数接收函数返回函数）")
    print("   ② @timer 只是 foo = timer(foo) 的语法糖")
    print("   ③ 装饰器【立即执行】，直接替换原函数")
    print("   ④ 不像 Java 注解要额外的'处理器'，装饰器本身就是处理逻辑")
